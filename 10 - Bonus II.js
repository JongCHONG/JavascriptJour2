var month = "October"

switch (month) {
  // winter
  case "December":
  case "January":
  case "February":
    console.log("Winter")
    break

  // spring
  case "March":
  case "April":
  case "May":
    console.log("Spring")
    break

  // summer
  case "June":
  case "July":
  case "August":
    console.log("Summer")
    break

  // Automn
  case "September":
  case "October":
  case "November":
    console.log("Autumn")
    break

  default:
    console.log("That's not a season")
}
